# multiple if statement
# Input: Get the user's age
age = int(input("Please enter your age: "))

# Multiple if statements checking different conditions
if age >= 18:
    print("You are an Adult.")

if age >= 16:
    print("You can drive.")

if age >= 18:
    print("You can vote.")

if age < 18:
    print("You are not an Adult.")
